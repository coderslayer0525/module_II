package ss8_cleanCode_mvc.entity;

public class Cars extends Vehicles {
    private int id;
    private String name;

    public Cars() {
    }

    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }
}

